ALTER TABLE EVENTO_SANITARIO DROP FOREIGN KEY IF EXISTS fk_evento_veterinario;
ALTER TABLE EVENTO_SANITARIO DROP FOREIGN KEY IF EXISTS fk_evento_insumo;
