        FOREIGN KEY (id_insumo) REFERENCES INSUMO(id_insumo)
        ON DELETE SET NULL,
    ADD CONSTRAINT fk_compra_proveedor
        FOREIGN KEY (id_proveedor) REFERENCES PROVEEDOR(id_proveedor)
        ON DELETE SET NULL;

