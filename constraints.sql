ALTER TABLE USUARIO_ROL DROP FOREIGN KEY IF EXISTS fk_usuariorol_usuario;
ALTER TABLE USUARIO_ROL DROP FOREIGN KEY IF EXISTS fk_usuariorol_rol;
ALTER TABLE USUARIO_ROL
