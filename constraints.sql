        FOREIGN KEY (id_usuario) REFERENCES USUARIO(id_usuario)
        ON DELETE CASCADE,
    ADD CONSTRAINT fk_usuariorol_rol
        FOREIGN KEY (id_rol) REFERENCES ROL(id_rol)
        ON DELETE CASCADE;

ALTER TABLE ASISTENCIA DROP FOREIGN KEY IF EXISTS fk_asistencia_empleado;
ALTER TABLE ASISTENCIA
    ADD CONSTRAINT fk_asistencia_empleado
        FOREIGN KEY (id_empleado) REFERENCES EMPLEADO(id_empleado)
        ON DELETE CASCADE;

ALTER TABLE PAGO_EMPLEADO DROP FOREIGN KEY IF EXISTS fk_pago_empleado;
