ALTER TABLE PRODUCCION_LECHE DROP FOREIGN KEY IF EXISTS fk_produccion_vaca;
ALTER TABLE PRODUCCION_LECHE DROP FOREIGN KEY IF EXISTS fk_produccion_lote;
ALTER TABLE PRODUCCION_LECHE MODIFY id_lote INT NULL;
ALTER TABLE PRODUCCION_LECHE
    ADD CONSTRAINT fk_produccion_vaca
        FOREIGN KEY (id_vaca) REFERENCES VACA(id_vaca)
        ON DELETE CASCADE,
    ADD CONSTRAINT fk_produccion_lote
        FOREIGN KEY (id_lote) REFERENCES LOTE_PRODUCCION(id_lote)
        ON DELETE SET NULL;

ALTER TABLE VENTA_LECHE DROP FOREIGN KEY IF EXISTS fk_venta_lote;
ALTER TABLE VENTA_LECHE DROP FOREIGN KEY IF EXISTS fk_venta_cliente;
ALTER TABLE VENTA_LECHE
    ADD CONSTRAINT fk_venta_lote
        FOREIGN KEY (id_lote) REFERENCES LOTE_PRODUCCION(id_lote)
        ON DELETE SET NULL,
    ADD CONSTRAINT fk_venta_cliente
        FOREIGN KEY (id_cliente) REFERENCES CLIENTE(id_cliente)
