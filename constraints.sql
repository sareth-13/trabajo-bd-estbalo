    ADD CONSTRAINT fk_pago_empleado
        FOREIGN KEY (id_empleado) REFERENCES EMPLEADO(id_empleado)
        ON DELETE SET NULL;

ALTER TABLE VACA DROP FOREIGN KEY IF EXISTS fk_vaca_madre;
ALTER TABLE VACA
    ADD CONSTRAINT fk_vaca_madre
        FOREIGN KEY (id_madre) REFERENCES VACA(id_vaca)
        ON DELETE SET NULL;

ALTER TABLE HISTORIAL_CORRAL DROP FOREIGN KEY IF EXISTS fk_historial_vaca;
ALTER TABLE HISTORIAL_CORRAL DROP FOREIGN KEY IF EXISTS fk_historial_corral;
